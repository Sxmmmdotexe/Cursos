print(f'Operador *= {mi_numero}')

#operador /= 
mi_numero /= 2 #mi numero = mi numero / 2
print(f'Operador /= {mi_numero}')

#operador **=
mi_numero **= 2 #mi numero = mi numero elevado a 2
print(f'Operador **= {mi_numero}')

#operador %= // si el valor resultante es 1 es inpar, si es 0 es par
