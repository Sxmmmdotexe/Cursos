#operadores de comparacion 

a = 5
b = 6

#primero se imprimen los valores y luego hacen la comparacion
print(f"{a} == {b} -> {a == b}")

print(f"{a} != {b} -> {a != b}")

print(f'{a} < {b} -> {a < b}')

print(f'{a} <= {b} -> {a <= b}')

print(f'{a} > {b} -> {a > b}')

print(f'{a} >= {b} -> {a >= b}')