dia_semana = int(input("Ingrese el valor del dia de la semana"))

if dia_semana == 1:
    print("Lunes")
elif dia_semana == 2:
    print("Martes")
elif dia_semana == 3:
    print("Miercoles")
elif dia_semana == 4:
    print('Jueves')
elif dia_semana == 5:
    print('Viernes')
elif dia_semana == 6:
    print('Sabado')
elif dia_semana == 7:
    print("Domingo")
else:
    print(f"Dia de la semana incorrecto , el valor ingresado es {dia_semana}")